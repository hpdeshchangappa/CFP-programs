import tkinter as tk

root=tk.Tk()
root.title("Tkinter CheckBox Example")
root.geometry("300x500")
root.config(bg="lightblue")

var1=tk.IntVar()
var2=tk.IntVar()
var3=tk.IntVar()

Checkbox1=tk.Checkbutton(root,text="Checkbox available",variable=var1,bg="light yellow")
Checkbox1.pack(padx=10, pady=10)
Checkbox2=tk.Checkbutton(root,text="Checkbox unavailable",variable=var2,bg="light yellow")
Checkbox2.pack(padx=10, pady=10)
Checkbox3=tk.Checkbutton(root,text="NONE OF THE ABOVE",variable=var3,bg="light yellow")
Checkbox3.pack(padx=10, pady=10)


root.mainloop()


















































































































































































































































































































































































































